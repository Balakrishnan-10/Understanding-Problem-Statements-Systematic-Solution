function differenceOfTwoIntegers(num1, num2) {
  console.log(num1, num2);
  num1 = Number(num1);
  num2 = Number(num2);
  return num1 - num2;
}
console.log(differenceOfTwoIntegers(5, 3));
console.log(differenceOfTwoIntegers(5, "8"));
console.log(differenceOfTwoIntegers("12", 3));

